#include <iostream>
using namespace std;

int main()
{
	int x = 3;
	int y = 9;
	int z = 77;

	if ( x == ( y / 3 ) )
	cout << "H";

	if ( z != 77 )
	cout << "q";

	if ( z == 77 )
	cout << "e";

	if ( z * y + x < 0 )
	cout << "g";

	if ( y == ( x * x ) )
	cout << "ll";

	cout << "o!" << endl;
} // end main
